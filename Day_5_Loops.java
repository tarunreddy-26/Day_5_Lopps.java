public class Day_5_Loops {
    public static void main(String[] args) {
        for(int i = 0; i <= 10; i++) {
            System.out.println("shekar");
        }
        String s = "phone";
        for (int i = 0; i < s.length(); i++) {
            System.out.println(s.charAt(3));
        }
        System.out.println("b" + 1 + "d" + 2);
    }
}
